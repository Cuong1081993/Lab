
function describeCountry(country, population, capitalCity) {
    return `${country} has ${population} and its capital city is ${capitalCity}`
}


const describeCountry_1 = describeCountry('VietNam', 500, 'Hue');
const describeCountry_2 = describeCountry('Japanese', 700, 'Tokyo');
const describeCountry_3 = describeCountry('Spanish', 900, 'Tokyo');

